<p style="text-align:left;color:black;text-align:justify">&emsp;&emsp;Cinsin Avrupa, Afrika, Asya, Avustralya ve Kuzey Amerika'da alt kozmopolit bir dağılımı vardır.</p>
<p><span>c</span></p>
<p style="text-align:left;color:black;text-align:justify">&emsp;&emsp;Mentha cinsini oluşturan türler geniş bir dağılıma sahiptir ve birçok ortamda bulunabilir. Çoğu, ıslak ortamlarda ve nemli topraklarda en iyi şekilde büyür. Nane 10-120 cm (4-48 inç) boyunda büyür ve belirsiz bir alana yayılabilir. Kontrolsüz yayılma eğilimleri nedeniyle, bazı naneler istilacı olarak kabul edilir.</p>


