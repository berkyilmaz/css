<h1>Nane Nedir ?</h1>        
<p style="text-align:left;color:black;text-align:justify">&emsp;&emsp;Nane, ballıbabagiller (Lamiaceae) familyasının Mentha cinsini oluşturan bütün Dünya'da görülebilen 25-30 türün ortak adı. Genellikle temmuz ve ağustos ayları arasında açarlar ve bu bitki uçucu yağ bakımından zengin nane kokulu yapraklar üretir.
Türler arasındaki kesin ayrım belirsizdir; 13 ila 24 tür olduğu tahmin edilmektedir.Melezleşme, bazı tür aralıkları çakıştığı doğal olarak oluşur. Birçok melez ve kültivar bilinmektedir.</p>
